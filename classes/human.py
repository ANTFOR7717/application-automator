class Human(object):
    def __init__(self, name):
        self.name = name
        self.gender = str()
        self.age = int()
        self.email = str()
        self.phone = int()

    def get_name(self):
        return self.name

    def get_gender(self):
        return self.gender

    def get_age(self):
        return self.age

    def get_email(self):
        return self.email

    def get_phone(self):
        return self.phone

